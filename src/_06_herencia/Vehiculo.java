package _06_herencia;

public class Vehiculo {
    private String patente;
    private String modelo;
    private int anio;
    private String color;

    public Vehiculo(String patente, String modelo, int anio, String color) {
        this.patente = patente;
        this.modelo = modelo;
        this.anio = anio;
        this.color = color;
    }
}
